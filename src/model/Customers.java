package model;

import java.util.*;

public class Customers
{
	//----------------------------------Attribute----------------------------------------------------------------------------------
	private ArrayList<Customer> customers;

	//----------------------------------Constructor--------------------------------------------------------------------------------	
	public Customers()
	{
		init();
	}

	private void init()
	{
		customers = new ArrayList<Customer>(50);		
	}
	//----------------------------------Getter-------------------------------------------------------------------------------------

	//----------------------------------Setter-------------------------------------------------------------------------------------

	//----------------------------------others-------------------------------------------------------------------------------------

	//----------------------------------Filesystem---------------------------------------------------------------------------------

	//----------------------------------toString-----------------------------------------------------------------------------------
	
	@Override
	public String toString()
	{
		StringBuilder builder = new StringBuilder();
		builder.append("Customers [customers=");
		builder.append(customers);
		builder.append("]");
		return builder.toString();
	}

}
